jQuery.validator.addMethod("applicationRedirectUris", function (value, element) {
    value = $.trim(value);
    var uris = value.replace(/\n{1,}/g, " ").replace(/\s{2,}/g," ").split(" ");
    for(var i=0;i<uris.length;i++){
        if(!uriRegExp.test(uris[i])){
            return false;
        }
    }
    return true;
}, "请输入正确格式的回调地址");


